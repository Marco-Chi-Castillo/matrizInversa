    private void btnLimpiarTablasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarTablasActionPerformed
           DefaultTableModel modelo = (DefaultTableModel) tablamatriz.getModel();
        while(modelo.getRowCount()>0)modelo.removeRow(0);
 
        TableColumnModel modCol = tablamatriz.getColumnModel();
        while(modCol.getColumnCount()>0)modCol.removeColumn(modCol.getColumn(0));
        
        TableColumnModel modCol2 = tablamatrizinversa.getColumnModel();
        while(modCol2.getColumnCount()>0)modCol2.removeColumn(modCol2.getColumn(0));
    }//GEN-LAST:event_btnLimpiarTablasActionPerformed

